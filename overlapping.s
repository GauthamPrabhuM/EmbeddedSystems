	;COPY FROM ONE LOCATION TO ANOTHER LOCATION WHERE IT HAS OVERLAPPING
	AREA	RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =SRC
	MOV R1, R0
	ADD R1, #(SIZE-OVER)*4
	MOV R4, R1
	MOV R2, #10
	ADD R4, #(SIZE-1)*4
	MOV R5,#1
L1	STR R5,[R0], #4
	ADD R5, R5, #1
	SUB R2, #1
	CMP R2, #0
	BNE L1
	LDR R0, =SRC
	MOV R2, #10
L2  LDR R3, [R0], #4
	STR R3, [R4], #-4
	SUB R2, #1
	CMP R2, #0
	BNE L2
STOP 
	B STOP
SIZE EQU 10
OVER EQU 2

	AREA mydata, DATA, READWRITE
SRC DCD 0

	END
